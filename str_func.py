def str_func(str):
    """
    Применяет верхний регистр ко всему входному тексту.
    :param str: Строка.
    :return: Строка в верхнем регистре.
    """
    return str.upper()

def text_func(str):
    """
    Применяет верхний регистр ко всем первым буквам.
    :param str: Строка.
    :return: Строка с верхним регистром каждой первой буквы.
    """
    return str.title()
